namespace Mana
{
    /// <summary>
    /// Represents a mouse button.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// Represents an unkown mouse button.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Represents the left mouse button.
        /// </summary>
        Left,

        /// <summary>
        /// Represents the middle mouse button.
        /// </summary>
        Middle,

        /// <summary>
        /// Represents the right mouse button.
        /// </summary>
        Right,
    }
}
